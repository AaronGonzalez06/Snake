package juegoserpiente;

/**
 *
 * @author Aaron
 */
public class Negra {

    private int posicionX;
    private int posicionY;
    private int posicionAuxX;
    private int posicionAuxY;

    public Negra(int posicionX, int posicionY, int posicionAuxX, int posicionAuxY) {
        this.posicionX = posicionX;
        this.posicionY = posicionY;
        this.posicionAuxX = posicionAuxX;
        this.posicionAuxY = posicionAuxY;
    }

    public int getPosicionX() {
        return posicionX;
    }

    public void setPosicionX(int posicionX) {
        this.posicionX = posicionX;
    }

    public int getPosicionY() {
        return posicionY;
    }

    public void setPosicionY(int posicionY) {
        this.posicionY = posicionY;
    }

    public int getPosicionAuxX() {
        return posicionAuxX;
    }

    public void setPosicionAuxX(int posicionAuxX) {
        this.posicionAuxX = posicionAuxX;
    }

    public int getPosicionAuxY() {
        return posicionAuxY;
    }

    public void setPosicionAuxY(int posicionAuxY) {
        this.posicionAuxY = posicionAuxY;
    }
    
    

}
